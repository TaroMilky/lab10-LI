"""
calculator.py
- Defines functions used to create a simple calculator

One function per operation, in order.
"""
# First example

import math

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def div(a, b):
    return a / b

def logarithm(a):
    return math.log(a)

def exp(a):
    return math.exp(a)

def square_root(a):
    return math.sqrt(a)

def hypotenuse(a, b):
    return math.hypot(a, b)


